#include "Students.h"

Student::Student(){
    id = 0;
    GPA=0;
    name = "";
    department = "";
}

//-------------------------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------------------------


Student::Student(int i , double g , string d , string n){
        id= i;
        GPA=g;
        department = d;
        name = n;
}

//-------------------------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------------------------


