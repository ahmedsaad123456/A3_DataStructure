bool Student::checkDepartment(string dep){
    return dep == "IS" || dep == "CS" || dep == "IT" || dep == "DS";
}

//-------------------------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------------------------

bool Student::checkGPA(double gpa){
    return gpa >= 0 && gpa<=4;
}

//-------------------------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------------------------


bool Student::checkID(int id){
    return id >= 1 && id<=100;
}

